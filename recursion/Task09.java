

public class Task09 {

	public static void main(String[] args) {
	    System.out.println(num(5, 10));
	}

	public static int num(int n, int sum) {
	    if (n == 0) {
	        return sum;
	    }

	    sum += n;
	    return num(n - 1, sum);

	}}

	    		
	    
	    

	       
	    

	 
	    

